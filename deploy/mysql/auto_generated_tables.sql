USE plm_sync_db;

SET NAMES utf8mb4;

-- CPCITEM表
DROP TABLE IF EXISTS `CPCITEM`;
CREATE TABLE `CPCITEM` (
    `ITEMID` DECIMAL(38,0) NOT NULL,
    `REV` DECIMAL(38,0),
    `INVORGID` DECIMAL(38,0),
    `ITEMCODE` VARCHAR(48),
    `ITEMNAME` TEXT,
    `ITEMENAME` TEXT,
    `ITEMDESC` TEXT,
    `DRAWID` VARCHAR(192),
    `REVLABEL` VARCHAR(48),
    `TSSTYPE` DECIMAL(38,0),
    `ITEMTYPE` DECIMAL(38,0),
    `ITEMUNIT` VARCHAR(24),
    `ITEMFLAG` DECIMAL(38,0),
    `STAT` DECIMAL(38,0),
    `ACTIVED` DECIMAL(38,0),
    `CLAS` DECIMAL(38,0),
    `CREATOR` VARCHAR(24),
    `CREATETIME` VARCHAR(30),
    `UPDATOR` VARCHAR(24),
    `UPDATETIME` VARCHAR(30),
    `DSGFLAG` VARCHAR(24),
    `PURCHFLAG` VARCHAR(24),
    `MANUFLAG` VARCHAR(24),
    `VENDOR` VARCHAR(192),
    `COST` DECIMAL(13,4),
    `GOALCOST` DECIMAL(13,4),
    `PRICE` DECIMAL(13,4),
    `WEIGHTUNIT` VARCHAR(24),
    `WEIGHT` DECIMAL(18,8),
    `MATERIAL` VARCHAR(192),
    `MATERIALTYPE` VARCHAR(12),
    `STANDARD` VARCHAR(48),
    `SPEC` TEXT,
    `STOCKITEM` DECIMAL(38,0),
    `NOTE` TEXT,
    `ITEMDEF1` TEXT,
    `ITEMDEF2` TEXT,
    `ITEMDEF3` TEXT,
    `ITEMDEF4` TEXT,
    `ITEMDEF5` TEXT,
    `ITEMCLASS` DECIMAL(38,0),
    `STOCKTYPE` DECIMAL(38,0),
    `LIFECYCLE` DECIMAL(38,0),
    PRIMARY KEY (`ITEMID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 ROW_FORMAT=DYNAMIC;
